package data;

public class Bateau {
	
	private String nom;
	
	

	public Bateau(String nom) {
		super();
		
	}

	public String getNom() {
		return nom;
	}

	public void setNom(String nom) {
		this.nom = nom;
	}

	@Override
	public String toString() {
		return "Bateas[nom=" + nom + "//]";
	}
	
	
	

}
